


function MousOverFace(){
	var short_description = document.getElementById('short_descript_face');
	var description   = document.getElementById('description_course_face');

	short_description.style.display = "none";
	description.style.display = "block";
}

function MousOutFace(){
	var short_description = document.getElementById('short_descript_face');
	var description   = document.getElementById('description_course_face');

	short_description.style.display = "block";
	description.style.display = "none";
}


